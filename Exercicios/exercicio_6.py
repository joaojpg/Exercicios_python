print('='*20, 'Descubra o antecessor e o sucessor do numero digitado', '='*20)
numero = int(input('Digite o valor desejado: '))
antecessor = numero - 1
sucessor = numero + 1
print('Este é o antecessor do numero digitado {}, este é o numero digitado {} e este é o numero sucessor {}' .format(antecessor, numero, sucessor))